#pragma once

namespace emulator6502
{
//////////////////////////////////////////////////////////////
///
/// @class AbstractComponent
///
//////////////////////////////////////////////////////////////
class AbstractComponent
{
  public:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Ensures proper cleanup of derived classes.
    ///
    //////////////////////////////////////////////////////////////
    virtual ~AbstractComponent() = default;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Pure virtual functions.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Init() = 0;
    virtual void Render() = 0;
    virtual void Destroy() = 0;
};
} // namespace emulator6502